# To print items in a list one after the other:
letters= ["a", "b", "c", "d", "e"]

for letter in letters:
    print(letter)

# To print a specific part of the list(slice):
for letter in letters [1:4]:
    print(letter)
    
# To get use a certain block of code repeatedly:
# range()
for number in range (9):
    print("Things will get better")

for number in range (5):
    print(number)