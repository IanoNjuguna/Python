# Comparison Operators

# {i} equal to operator
# (a) true
print(1==1)

# (b) false
print(1==2)

# (c) comparing strings;{true}
print("hello"=="hello")

# (d) comparing strings;{false}
print("hello"=="helo")


# {ii} not equal to operator
#(a) true
print(1!=2)

#(b) false
print(2!=2)

#(c) comparing strings;{true}
print("hello"!="helo")


# {iii} more than operator
# (a) true
print(2>1)

# (b) false
print(1>2)


# {iv} more than OR equal to operator
# (a) true
print(2>=2)


# {v} less than operator
# (a) true
print(1<2)


# {vi} less than OR equal to operator
# (a) true
print(1<=1)